# =====================
# Git 常用命令速查表
# =====================

git_guide = """
================================================================================
                            GIT 常用命令速查表
================================================================================

【初始化和配置】
--------------------------------------------------------------------------------
git init                      # 在当前目录初始化新仓库
git clone <url>               # 克隆远程仓库
git config --global user.name "名字"   # 设置用户名
git config --global user.email "邮箱"   # 设置邮箱

【基本操作】
--------------------------------------------------------------------------------
git status                    # 查看当前状态
git add <file>               # 添加文件到暂存区
git add .                    # 添加所有文件
git commit -m "提交信息"       # 提交到本地仓库
git commit -am "信息"        # 添加并提交（仅追踪过的文件）

【查看历史】
--------------------------------------------------------------------------------
git log                      # 查看提交历史
git log --oneline            # 简洁模式
git log -n 5                # 最近5条
git diff                    # 查看未提交的修改
git diff --staged           # 查看暂存区的修改

【分支操作】
--------------------------------------------------------------------------------
git branch                   # 查看分支
git branch <name>            # 创建新分支
git checkout <branch>       # 切换分支
git checkout -b <name>      # 创建并切换
git merge <branch>          # 合并分支
git branch -d <branch>      # 删除分支

【远程操作】
--------------------------------------------------------------------------------
git remote -v               # 查看远程仓库
git remote add origin <url>  # 添加远程仓库
git push                    # 推送到远程
git push -u origin main     # 首次推送并设置上游
git pull                    # 拉取并合并
git fetch                   # 仅拉取不合并
git clone <url>             # 克隆仓库

【撤销和回退】
--------------------------------------------------------------------------------
git checkout -- <file>       # 撤销未暂存的修改
git reset HEAD <file>        # 取消暂存
git reset --soft HEAD~1     # 撤销上次提交（保留修改）
git reset --hard HEAD~1     # 撤销上次提交（删除修改）

【标签】
--------------------------------------------------------------------------------
git tag v1.0                # 创建标签
git tag                    # 查看标签
git push origin v1.0       # 推送标签

================================================================================
"""

print(git_guide)

