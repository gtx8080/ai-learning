http_guide = """
================================================================================
                            HTTP 基础速查表
================================================================================

【HTTP 方法】
--------------------------------------------------------------------------------
GET     - 获取资源          (浏览网页、查询数据)
POST    - 创建资源          (注册用户、提交表单)
PUT     - 更新资源          (修改全部内容)
PATCH   - 部分更新          (修改部分内容)
DELETE  - 删除资源          (删除数据)

【HTTP 状态码】
--------------------------------------------------------------------------------
2xx - 成功
  200 OK           - 请求成功
  201 Created     - 创建成功
  204 No Content  - 删除成功

4xx - 客户端错误
  400 Bad Request   - 请求格式错误
  401 Unauthorized  - 未认证
  403 Forbidden     - 无权限
  404 Not Found     - 资源不存在

5xx - 服务器错误
  500 Internal Server Error - 服务器内部错误
  502 Bad Gateway          - 网关错误
  503 Service Unavailable  - 服务不可用

【HTTP 请求结构】
--------------------------------------------------------------------------------
GET /api/users HTTP/1.1
Host: api.example.com
Authorization: Bearer <token>
Accept: application/json

【HTTP 响应结构】
--------------------------------------------------------------------------------
HTTP/1.1 200 OK
Content-Type: application/json
Content-Length: 123

{
  "code": 0,
  "data": {
    "name": "张三",
    "age": 25
  }
}

【常见请求头】
--------------------------------------------------------------------------------
Content-Type    - 请求内容类型
Authorization  - 认证信息
Accept         - 接受的内容类型
User-Agent    - 客户端信息

【常见响应头】
--------------------------------------------------------------------------------
Content-Type   - 响应内容类型
Content-Length - 内容长度
Cache-Control  - 缓存策略
Set-Cookie     - 设置 Cookie

================================================================================

【一个完整的 HTTP 请求示例】
 --------------------------------------------------------------------------------

请求:
GET /users/1 HTTP/1.1
Host: api.example.com
Accept: application/json

响应:
HTTP/1.1 200 OK
Content-Type: application/json

{
  "id": 1,
  "name": "张三",
  "email": "zhangsan@example.com"
}

================================================================================

【RESTful API 设计原则】
--------------------------------------------------------------------------------
1. 使用名词而非动词:  /users  而非  /getUsers
2. 使用 HTTP 方法:    GET/POST/PUT/DELETE
3. 使用复数形式:     /users  而非  /user
4. 嵌套资源:         /users/1/orders
5. 使用查询参数:     /users?age=25&city=北京

示例:
  GET    /users          - 获取用户列表
  GET    /users/1       - 获取ID为1的用户
  POST   /users          - 创建新用户
  PUT    /users/1       - 更新用户
  DELETE /users/1       - 删除用户

================================================================================
"""

print(http_guide)
